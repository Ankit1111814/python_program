# ndim find the dimension of the array

import numpy as np
arr1=np.array([2,4,5]) # Creates a 1D array
arr2= np.array([[2, 4, 3], [3, 2, 1], [1, 2, 3]])
print(arr1.ndim)  # Prints the number of dimensions of the array
print(arr2.ndim)  # Prints the number of dimensions of the array
print(arr1)  # Displays the 1D array
print(arr2)  # Displays the 2D array
